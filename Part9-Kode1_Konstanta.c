#include <stdio.h>

#define PANJANG 2
#define LEBAR 1
#define TINGGI 4

int main(void)
{
    printf("Panjang = %d" ,PANJANG);
    printf("\n");

    printf("Lebar = %d" ,LEBAR);
    printf("\n");

    printf("Tinggi = %d" ,TINGGI);
    printf("\n");

    printf("Volume (Panjang*Lebar*Tinggi) = %d" ,PANJANG*LEBAR*TINGGI);
    printf("\n");

    return 0;
}
